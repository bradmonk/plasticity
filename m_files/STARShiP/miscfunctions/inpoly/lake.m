function node = makeNode(tmp)

next  = 1;
lastx = inf;
lasty = inf;
for i = 1:size(tmp,1)
    for j = 1:4
        % Current xy
        x = tmp(i,2*j-1);
        y = tmp(i,2*j);
        % Check for zero
        if (x~=0)||(y~=0)
            % Add if not a duplicate
            if (x~=lastx)&&(y~=lasty)
                % Add to list
                node(next,1) = x; lastx = x;
                node(next,2) = y; lasty = y;
                % Counter
                next = next+1;
            end
        end
    end
end
node(:,1) = -node(:,1);

if (node(end,1)==node(1,1))&&(node(end,2)==node(1,2))
    node = node(1:end-1,:);
end